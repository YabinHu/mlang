//===--------------------------------------------------------------------===//
// UserClassDefn
//===--------------------------------------------------------------------===//
void
UserClassDefn::setBases(ClassBaseSpecifier const * const *CBases,
                        unsigned NBases) {
  ASTContext &C = getASTContext();


  if (!Bases.isOffset() && NBases > 0)
    C.Deallocate(getBases());


  Bases = new(C) ClassBaseSpecifier [NBases];
  NumBases = NBases;
  for (unsigned i = 0; i < NumBases; ++i) {
    getBases()[i] = *CBases[i];
  }

  return;
}


